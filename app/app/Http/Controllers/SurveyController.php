    public function store(Request $request)
    {
        $survey = Survey::create([
            'slug'        => Str::random(10),
            'title'       => $request->title,
            'description' => $request->description,
            'content_ids' => $request->content_ids,
            'duration'    => implode('|', (array) $request->duration),
        ]);

        return response()->json([
            'message'=> 'success',
        ], 200);
    }

